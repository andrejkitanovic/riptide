'use strict';

if (gsap) {
    $(function () {
        const header = $('.header');

        const loseState = () => {
            $(header)[0].classList.add('hide-top');
        };

        ScrollTrigger.create({
            start: 'top top',
            end: document.innerHeight,
            onUpdate: (self) => {
                self.direction === -1 ? $(header)[0].classList.remove('hide-top') : loseState();
            }
        });
    });
}
