'use strict';

if (gsap) {
  $(function () {
    const list = $('.list');
    const listItem = $('.list__item');

    if (list.length > 0) {
      var tl = gsap.timeline({
        scrollTrigger: {
            trigger: list,
        }
      });

      tl.from(listItem, {
        autoAlpha: 0,
        ease: 'power3',
        y:20,
        stagger: 0.3,
        duration: 0.3,
      });

    }
  });
}
